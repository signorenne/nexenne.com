// The language is driven by the URL (`/` is English, `/it/` is Italian), so the
// store is set from the route param in the root layout (see +layout.svelte). It
// starts at 'en' to match the prerendered SSR default for the unprefixed routes.
export const lang = writable<Lang>('en');

if (browser) {
	// Keep the <html lang> attribute in sync for a11y / correct hyphenation.
	lang.subscribe((l) => {
		try {
			document.documentElement.setAttribute('lang', l);
		} catch {}
	});
}

export const t = derived(lang, ($lang) => {
	return (key: string): string => DICT[$lang]?.[key] ?? DICT.en[key] ?? key;
});

export function currentLang(): Lang {
	return get(lang);
}

/**
 * Find the first supported language among the browser's preferred tags.
 *
 * @param tags Browser language tags, for example from navigator.languages.
 * @return The first supported language, or null when none is supported.
 */
export function preferredLang(tags: readonly string[]): Lang | null {
	for (const tag of tags) {
		const base = tag?.toLowerCase().split('-')[0];
		if (base === 'en' || base === 'it') return base;
	}
	return null;
}

/**
 * Decide which language to suggest switching to. Returns the browser's preferred
 * language only when it is supported and differs from the current page language.
 *
 * @param current The language currently being shown.
 * @param tags    Browser language tags, for example from navigator.languages.
 * @return The language to suggest, or null when no switch should be offered.
 */
export function suggestLang(current: Lang, tags: readonly string[]): Lang | null {
	const pref = preferredLang(tags);
	return pref && pref !== current ? pref : null;
}
